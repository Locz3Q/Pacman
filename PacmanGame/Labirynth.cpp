bool Labirynth::teleport(long x, long y, char d) {
	if (d == 'l') {
		if (this->tileMapCoords[y][x - 1] == 2) {
			return true;
		}
		else {
			return false;
		}
	}

	else if (d == 'r') {
		if (this->tileMapCoords[y][x + 1] == 2) {
			return true;
		}
		else {
			return false;
		}
	}
}

bool Labirynth::findZero(int y, int x) {
	if (this->tileMapCoords[y][x] == 0) {
		return true;
	}
	else { 
		return false; 
	}
}

void Labirynth::showMap(Labirynth col) {
	for (int i = 1; i < 30; i++) {
		for (int j = 1; j < 27; j++) {
			cout << col.tileMapCoords[i][j] << " ";
		}
		cout << "\n";
	}
}